				<h2 class="text-info">CÓDIGO DE ÉTICA DEL SISTEMA NACIONAL DE EVALUACIÓN CIENTÍFICA Y TECNOLÓGICA</h2><br>

				<!-- INICIA PÁRRAFO -->
				<p class="text-justify">
					<h4>EXPOSICIÓN DE MOTIVOS</h4>
					Con base en las atribuciones que le confieren la Ley de Ciencia y Tecnología, la Ley Orgánica del
					Consejo Nacional de Ciencia y Tecnología y su Estatuto Orgánico, el CONACYT constituyó el Sistema
					Nacional de Evaluación Científica y Tecnológica (SINECYT), como una instancia permanente de
					coordinación y consulta conformada por un Consejo Directivo, un Consejo de Acreditación de
					Evaluadores, Comités de Acreditación de Evaluadores, el Registro CONACYT de Evaluadores
					Acreditados (RCEA) y las Comisiones de Evaluación de los Fondos y Programas del CONACYT, cuyo
					objeto es diseñar, recomendar, impulsar, homologar y emitir criterios, prácticas, metodologías e
					instrumentos para realizar las evaluaciones ex-ante, y ex-post de los apoyos otorgados por el Consejo. <br><br>
					La propia Ley de Ciencia y Tecnología establece, como principio orientador de los apoyos otorgados por
					el CONACYT, que la selección de instituciones, programas, proyectos y personas destinatarios de los
					mismos, se realice mediante procedimientos competitivos, eficientes, equitativos y públicos, sustentados
					en méritos y calidad, así como orientados con un claro sentido de responsabilidad social.<br><br>
					Asimismo, en cuanto a la evaluación técnica y científica de los proyectos apoyados por los Fondos
					CONACYT, la Ley establece que la integración de comisiones de evaluación se formará por
					investigadores científicos y tecnólogos del sector correspondiente designados de común acuerdo entre la
					entidad y el CONACYT. Estas comisiones actuarán de conformidad con las reglas de operación
					establecidas para cada Fondo y contarán con el apoyo del Consejo, a través de los Secretarios
					Técnicos.<br><br>
					Por su parte, la Ley Orgánica del CONACYT señala la facultad del Consejo, para emitir los criterios
					generales, términos de referencia y parámetros de evaluación para medir el impacto, los resultados y
					beneficios de los apoyos otorgados para la investigación científica y tecnológica.<br><br>
					El Estatuto Orgánico, establece la atribución del Consejo para conducir y operar el SINECYT, fijando sus
					objetivos, funciones y forma de organización dentro de las disposiciones normativas que para el efecto
					expida, las cuales deberán ser aprobadas por la Junta de Gobierno del CONACYT. Estas disposiciones
					se encuentran contenidas en las “Bases de Organización y Funcionamiento del SINECYT”.<br><br>
					El adecuado funcionamiento del SINECYT, implica un elevado nivel de compromiso por parte de todos
					los individuos que participan en los distintos cuerpos colegiados del sistema, así como contar con reglas
					claras que normen la actuación de cada uno de los participantes. En este contexto, se establece el
					presente Código de Ética, el cual tiene como propósito determinar los lineamientos de conducta a los
					que deben apegarse los integrantes del SINECYT para cumplir cabalmente con las funciones y
					compromisos adquiridos en cada instancia.<br><br> Los miembros del SINECYT, y en particular del RCEA, deben mantener
					en todo momento una actitud que implique los siguientes valores:<br><br>

					<ul>
						<li>Objetividad</li>
						<li>Independencia</li>
						<li>Profesionalismo</li>
						<li>Excelencia</li>
						<li>Transparencia</li>
						<li>Compromiso</li>
						<li>Honestidad</li>
						<li>Justicia</li>
						<li>Igualdad</li>
						<li>Confidencialidad</li>
					</ul>

					En este sentido, se requiere que cada integrante del SINECYT, en su actuar tome en cuenta lo siguiente:


					<h5><br><br>1. OBJETIVIDAD</h5>
					Es la actitud que consiste en fundamentar sus decisiones como evaluador en los méritos concretos
					del proyecto o solicitud, para lo cual deberá de:<br><br>
					<ul>
						<li>Llevar a cabo los procesos de evaluación de candidatos, propuestas y proyectos, bajo los
						principios de pluralidad académica y metodológica, imparcialidad y transparencia;</li>
						<li>Ser precisos en las evaluaciones realizadas;</li>
						<li>Excusarse de evaluar propuestas enviadas por investigadores o grupos que representen
							competencia, rivalidad institucional, o con las que guarden vínculos laborales, de parentesco,
						amistad o enemistad;</li>
						<li>Escuchar con respeto a sus pares, escuchar con atención y apertura de entendimiento en sus
						planteamientos y dialogar con razones y tolerancia; y</li>
						<li>Actuar con serenidad de ánimo y equilibrio interno, a fin de que sus evaluaciones estén
						desprovistas de aprensiones y prejuicios.</li>
					</ul>


					<h5><br><br>2. INDEPENDENCIA</h5>
					Consiste en que, durante los procesos de evaluación, se tome en cuenta las características y nivel de
					desarrollo de la disciplina o conjunto disciplinario en el cual se aplica, sus posibilidades y condiciones
					para la formación de recursos humanos, de grupos y redes de investigación y/o desarrollo tecnológico,
					así como la originalidad, calidad, productividad, trascendencia e impacto de los resultados o productos
					que se hayan obtenido en materia de investigación científica y/o desarrollo tecnológico, sin tomar en
					cuenta presiones o intereses extrañas a los evaluadores. Por lo tanto, éstos deberán:<br><br>
					<ul>
						<li>Rechazar cualquier tipo de recomendación que tienda a influir en la evaluación de los proyectos
							que se sometan a su consideración. Incluso las que pudieran provenir de los servidores públicos
						del CONACYT;</li>
						<li>Preservar el recto ejercicio de su función denunciando cualquier acto que tienda a afectar su
						independencia;</li>
						<li>Evitar involucrarse en actividades o situaciones que pudieran directa e indirectamente afectar su
						independencia; y</li>
						<li>Abstenerse de recomendar, insinuar o sugerir a los demás integrantes del SINECYT, el sentido
						en que deban emitir sus dictámenes al realizar una evaluación.</li>
					</ul>


					<h5><br><br>3. PROFESIONALISMO</h5>
					Es la disposición de ejercer de manera responsable los procedimientos de evaluación que se tienen
					encomendados, con capacidad y aplicación. Por lo tanto, el evaluador deberá:<br><br>
					<ul>
						<li>Abstenerse de cualquier acto que pueda mermar la respetabilidad propia de su encargo, tanto en
						el ámbito público como en el privado;</li>
						<li>Actualizar permanentemente sus conocimientos;</li>
						<li>Respetar la libertad académica y el pluralismo metodológico;</li>
						<li>Estudiar con acuciosidad los proyectos que deba de evaluar;</li>
						<li>Fundar y motivar sus evaluaciones;</li>
						<li>Dedicar el tiempo necesario para el despacho de los proyectos que deba de evaluar;</li>
						<li>Asumir responsablemente las consecuencias de sus decisiones;</li>
						<li>Aceptar sus errores y aprender de ellos para mejorar su desempeño;</li>
						<li>Cumplir con el compromiso adquirido;</li>
						<li>Entregar los resultados de las evaluaciones efectuadas en el caso de las candidaturas,
							propuestas, programas o proyectos asignados, dentro de los periodos que establezcan las
							convocatorias emitidas, con el fin de colaborar a que los procesos concluyan en los tiempos
						establecidos;</li>
						<li>Llevar a cabo de manera personalísima las funciones que tiene encomendadas; y</li>
						<li>Evitar el surgimiento de conflicto entre pares, para lo cual se deberá tomar en cuenta la lealtad y
						colaboración mutua.</li>
					</ul>


					<h5><br><br>4. EXCELENCIA</h5>
					Todo integrante del SINECYT se perfecciona cada día para desarrollar las siguientes virtudes:<br><br>

					<ul>
						<li>Prudencia: En los procesos de evaluación recoge la información a su alcance con criterios rectos
							y objetivos; pondera las consecuencias favorables y desfavorables que puedan producirse por su
						decisión, y luego toma ésta y actúa conforme a lo decidido;</li>
						<li>Responsabilidad: Asume plenamente las consecuencias de sus actos;</li>
						<li>Orden: Mantiene la adecuada organización y planificación en el trabajo a su cargo;</li>
						<li>Decoro: Cuida su comportamiento habitual, tanto en su vida pública como privada;</li>
						<li>Laboriosidad: Cumple diligentemente con sus obligaciones; y</li>
						<li>Honestidad: Observa un comportamiento recto y honrado.</li>
					</ul>


					<h5><br><br>5. TRANSPARENCIA</h5>
					Lo que significa que los integrantes del SINECYT, se guiarán por los criterios, instrumentos y mecanismos estandarizados, para lo cual, cada evaluador:<br><br>

					<ul>
						<li>Al emitir dictámenes se apegará a los criterios de elegibilidad establecidos por el SINECYT,
						utilizando un lenguaje apropiado y respetuoso;</li>
						<li>Utilizará los valores de ponderación aprobados por el Comité de Acreditación correspondiente; y</li>
						<li>Realizará evaluaciones que describan con precisión el trabajo desarrollado, las conclusiones
						alcanzadas y el contexto en el que se basaron para emitir sus dictámenes.</li>
					</ul>


					<h5><br><br>6. COMPROMISO</h5>
					Los integrantes del SINECYT deberán tener presente el reconocimiento que les ha conferido el CONACYT, reconocimiento del que se advierte la confianza y respeto y confianza que en ellos se ha depositado, por lo que en todo momento deberán:<br><br>

					<ul>
						<li>Estar comprometidos con el desarrollo de la ciencia y la tecnología a nivel institucional, estatal, regional, institucional, sectorial y nacional;</li>
						<li>Desarrollar su función de manera dedicada y responsable;</li>
						<li>Contar con suficiente experiencia profesional para evaluar las propuestas, proyectos o
						candidaturas presentadas; y</li>
						<li>Cumplir con los preceptos establecidos en el Código de Ética.</li>
					</ul>


					<h5><br><br>7. HONESTIDAD</h5>
					Lo cual implica que los evaluadores deberán:<br><br>
					<ul>
						<li>Mantener un principio de probidad en la evaluación de los alcances científicos que se presentan
						en las solicitudes, propuestas y proyectos;</li>
						<li>Evitar favorecer propuestas o grupos de investigación con quienes estén involucrados personal o
						laboralmente;</li>
						<li>Abstenerse de descalificar a personas, investigadores, instituciones, o unidades de investigación
						por razones personales o institucionales;</li>
						<li>Excusarse de evaluar propuestas en las cuales participen, directa o indirectamente, ni en
						aquellas que sean presentadas por la institución donde se encuentren adscritos;</li>
						<li>No involucrarse en aspectos económicos con los participantes del proyecto, en particular cuando
						se trate de los sectores productivos; y</li>
						<li>Respetar la propiedad intelectual, absteniéndose de realizar cualquier acto de divulgación de los
						proyectos que deban dictaminar.</li>
					</ul>


					<h5><br><br>8. JUSTICIA</h5>
					Los integrantes del SINECYT, se esforzarán por darle a cada quien lo que le corresponde, por lo que en todo momento los evaluadores deberán:<br><br>

					<ul>
						<li>Conocer, cumplir y hacer cumplir la normatividad a que deberá apegarse el proceso de
						evaluación;</li>
						<li>Ser justos al momento de emitir el dictamen sobre las evaluaciones realizadas; y</li>
						<li>No permitir que influya ningún aspecto o tendencia personal, institucional o de grupo que impida emitir un juicio correcto sobre las evaluaciones realizadas.</li>
					</ul>


					<h5><br><br>9. IGUALDAD</h5>
					El evaluador, en todo momento deberá abstenerse de utilizar su posición para favorecer a proyectos específicos, por lo que deberá:<br><br>

					<ul>
						<li>Actuar en forma equitativa, sin conceder preferencias o privilegios indebidos a personas, grupos,
						proyectos, programas, o instituciones, sin tomar en cuenta intereses personales;</li>
						<li>Mantener siempre una actitud propositiva en cada una de las etapas de evaluación en que
						participen; y</li>
						<li>Aceptar la pluralidad académica y divergencia de opiniones.</li>
					</ul>


					<h5><br><br>10. CONFIDENCIALIDAD</h5>
					En aras de preservar los derechos de propiedad industrial e intelectual de los proyectos que se sometan a su consideración, cada evaluador deberá:<br><br>

					<ul>
						<li>Mantener en secreto la información sobre las propuestas recibidas, aceptadas, evaluadas y
						resultados obtenidos;</li>
						<li>La información contenida en las propuestas o proyectos del proceso de evaluación es
						estrictamente confidencial y no puede difundirse bajo ningún medio o concepto; y</li>
						<li>No difundir la información de las propuestas del proceso de evaluación, ni de sus resultados para
							evitar conflictos, por lo que los resultados de dichos procesos que se lleven a cabo, sólo podrán
						difundirse por los medios oficiales autorizados para ello.</li>
					</ul>


					<h5><br><br>11. SANCIONES</h5>
					Además de las sanciones previstas en los ordenamientos jurídicos, las violaciones a este Código de Ética pueden dar lugar a sanciones disciplinarias que aplicará el Consejo Directivo del SINECYT, que podrán consistir en:<br><br>

					<ul>
						<li>Advertencia privada,</li>
						<li>Advertencia pública,</li>
						<li>Amonestación pública,</li>
						<li>Suspensión temporal del reconocimiento de evaluador, o</li>
						<li>Baja definitiva del evaluador en dentro del Registro CONACYT de Evaluadores Acreditados,</li>
					</ul>

					<br>Lo anterior se hace del conocimiento de todos y cada uno de los integrantes del SINECYT que participan
					en cada una de las etapas de los procesos de evaluación, quienes se comprometen a cumplir y hacer
					cumplir el presente Código de Ética.<br><br>

